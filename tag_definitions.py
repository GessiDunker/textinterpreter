def get_full_tagged_counts(tag_counts):
    full_tagged_counts = Counter()
    for tag, count in tag_counts.items():
        full_tag_name = tag_full_names.get(tag)

        if full_tag_name is None:
            print(f"Unknown tag: {tag}")
            full_tag_name = "Unknown"        

        full_tagged_counts[full_tag_name] += count
    return full_tagged_counts